public class Q11_Print_No_1_50 {
    static void main(String[] args) {

        System.out.println("----Using For Loop-----");

        for (int i = 1; i <=50; i++) {
            System.out.println(i);
        }

        System.out.println("----Using While Loop-----");

        int i = 1;
        while(i <= 50) {
            System.out.println(i);
            i++;
        }

    }
}
